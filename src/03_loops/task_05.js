/* Принимать пользовательский ввод последовательности целых чисел, при этом окончанием последовательности служит пустая строка (пользователь нажимает Enter).
 * Определить наибольшее введенное значение и количество его повторений в последовательности.
 * Проверять вводимые данные на валидность, в случае неверного ввода перезапрашивать данные у пользователя.
 * Вывод осуществлять с помощью модального окна
*/

let maxValue = null;
let maxCount = 0;
let validCount = 0;

while (true) {
  const input = prompt("Введите целое число (или нажмите Enter для завершения):");
  if (input === null || input.trim() === "") {
    break; // конец ввода
  }

  const num = Number(input);

  if (isNaN(num) || !Number.isInteger(num)) {
    alert("Это не целое число. Попробуйте снова.");
    continue;
  }

  validCount++;

  if (maxValue === null || num > maxValue) {
    maxValue = num;
    maxCount = 1;
  } else if (num === maxValue) {
    maxCount++;
  }
}

if (maxValue !== null) {
  alert(`Наибольшее введённое значение: ${maxValue}\nКоличество его повторений: ${maxCount}\nОбщее количество введённых чисел: ${validCount}`);
} else {
  alert("Вы не ввели ни одного числа.");
}

