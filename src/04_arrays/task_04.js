/* Сформировать двумерный массив чисел на основе данных, вводимых пользователем.
 * Количество строк и столбцов должно определяться пользователем.
 * Вывести итоговый массив в консоль в виде таблицы.
 */

// Запрос количества строк и столбцов у пользователя
const rows = parseInt(prompt("Введите количество строк:"));
const cols = parseInt(prompt("Введите количество столбцов:"));

const array = [];

// Заполняем двумерный массив числами, введёнными пользователем
for (let i = 0; i < rows; i++) {
  array[i] = [];
  for (let j = 0; j < cols; j++) {
    array[i][j] = parseInt(prompt(`Введите элемент [${i}][${j}]:`));
  }
}

// Выводим итоговый массив в консоль
console.log(array);

// Вывод двумерного массива в виде таблицы с помощью console.log
for (let i = 0; i < rows; i++) {
  // Преобразуем строку массива в строку с разделителями, например, пробелами
  const rowString = array[i].join(' ');
  console.log(rowString);
}

