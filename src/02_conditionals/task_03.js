/* Вывести корни квадратного уравнения по введенным пользователем коэффициентам. */

const a = Number(prompt("Введите коэффициент a:"));
const b = Number(prompt("Введите коэффициент b:"));
const c = Number(prompt("Введите коэффициент c:"));

if (isNaN(a) || isNaN(b) || isNaN(c)) {
  alert("Пожалуйста, введите корректные числовые значения");
}
else if (a === 0) {
  alert("Коэффициент a не должен быть равен 0 (не квадратное уравнение)");
}
else {
  const discriminant = b * b - 4 * a * c;

  if (discriminant > 0) {
    const root1 = (-b + Math.sqrt(discriminant)) / (2 * a);
    const root2 = (-b - Math.sqrt(discriminant)) / (2 * a);
    alert(`Два корня: x1 = ${root1}, x2 = ${root2}`);
  } else if (discriminant === 0) {
    const root = -b / (2 * a);
    alert(`Один корень: x = ${root}`);
  } else {
    alert("Действительных корней нет");
  }
}

